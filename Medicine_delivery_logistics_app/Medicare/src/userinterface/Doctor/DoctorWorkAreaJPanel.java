    private void workRequestJTableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_workRequestJTableMouseClicked
        // TODO add your handling code here:

        int selectedrow = workRequestJTable.getSelectedRow();

        if (selectedrow >= 0) {

            DoctorTreatmentWorkRequest docrequest = (DoctorTreatmentWorkRequest) workRequestJTable.getValueAt(selectedrow, 0);
            firstnametextfield1.setText(docrequest.getFirstName());
            middlenametextfield1.setText(docrequest.getMiddleName());
            lastnametextfield1.setText(docrequest.getLastName());
            agetextfield1.setText(String.valueOf(docrequest.getAge()));
            sextextfield1.setText(docrequest.getSex());
            addressline1textfield1.setText(docrequest.getAddressLine1());
            addressline2textfield1.setText(docrequest.getAddressLine2());
            citytexxtfield1.setText(docrequest.getCity());
            statetextfield1.setText(docrequest.getState());
            countrytextfield1.setText(docrequest.getCountry());
            dobtextfield1.setText(docrequest.getDOB());
            phonenotextfield1.setText(String.valueOf(docrequest.getPhoneNo()));
            occupationTextField1.setText(docrequest.getOccupation());
            lasttimeTextField.setText(docrequest.getLastTimeDisease());

            resultJTextField1.setText(docrequest.getTestResult());
            freemedicineTextField1.setText(docrequest.getFreeMed());
            QuantityTextfield1.setText(String.valueOf(docrequest.getQuantity()));
            nextTextField1.setText(docrequest.getAppointmentDate());
            commentsTextFiled.setText(docrequest.getComments());

            if (docrequest.getMaritalStatus().equals(jRadioButton2.getText())) {
                jRadioButton2.setSelected(true);
            } else if (docrequest.getMaritalStatus().equals(jRadioButton3.getText())) {
                jRadioButton3.setSelected(true);
            } else if (docrequest.getMaritalStatus().equals(jRadioButton4.getText())) {
                jRadioButton4.setSelected(true);
            } else {
                jRadioButton1.setSelected(true);
            }

        } else {
            JOptionPane.showMessageDialog(null, "Please Select any row");
        }
    }//GEN-LAST:event_workRequestJTableMouseClicked

