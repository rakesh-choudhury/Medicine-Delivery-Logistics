    private void SelectPlan(String typeOfProvider, String typeOfPlan) {
        if (typeOfProvider.equals("UCI Health") && typeOfPlan.equals("Psychological Services")) {
            txtPlanCost.setText("100");
        } else if (typeOfProvider.equals("UCI Health") && typeOfPlan.equals("Flu shot")) {
            txtPlanCost.setText("20");
        } else if (typeOfProvider.equals("UCI Health") && typeOfPlan.equals("Physical Examination")) {
            txtPlanCost.setText("50");
        }
        if (typeOfProvider.equals("American College of Physicians") && typeOfPlan.equals("Psychological Services")) {
            txtPlanCost.setText("80");
        } else if (typeOfProvider.equals("American College of Physicians") && typeOfPlan.equals("Flu shot")) {
            txtPlanCost.setText("30");
        } else if (typeOfProvider.equals("American College of Physicians") && typeOfPlan.equals("Physical Examination")) {
            txtPlanCost.setText("60");
        }
        if (typeOfProvider.equals("Primary Care") && typeOfPlan.equals("Psychological Services")) {
            txtPlanCost.setText("70");
        } else if (typeOfProvider.equals("Primary Care") && typeOfPlan.equals("Flu shot")) {
            txtPlanCost.setText("30");
        } else if (typeOfProvider.equals("Primary Care") && typeOfPlan.equals("Physical Examination")) {
            txtPlanCost.setText("90");
        }
    }

