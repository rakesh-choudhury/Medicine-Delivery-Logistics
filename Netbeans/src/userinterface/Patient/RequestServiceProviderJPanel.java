    private void btnRequestServiceActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRequestServiceActionPerformed
        // TODO add your handling code here:
        if (txtPlanCost.getText() != null && !txtPlanCost.getText().isEmpty()) {
            String typeOfProvider = cmbTypeOfProvider.getSelectedItem().toString();
            String typeOfPlan = cmbTypeOfPlan.getSelectedItem().toString();
            CarrierWorkRequest carrierWorkRequest = new CarrierWorkRequest();
            carrierWorkRequest.setTypeOfProvider(typeOfProvider);
            carrierWorkRequest.setTypeOfPlan(typeOfPlan);
            // SelectPlan(typeOfProvider, typeOfPlan);
            carrierWorkRequest.setSender(userAccount);
            carrierWorkRequest.setStatus("Sent");
            carrierWorkRequest.setCost(Integer.parseInt(txtPlanCost.getText()));

            Organization org = null;
            for (Network network : business.getNetworkList()) {
                for (Enterprise enterprise : network.getEnterpriseDirectory().getEnterpriseList()) {
                    if (enterprise instanceof HealthCampsEnterprise) {
                        for (Organization organization : enterprise.getOrganizationDirectory().getOrganizationList()) {
                            if (organization instanceof HealthCampsOrganization) {
                                org = organization;
                                break;
                            }
                        }
                    }

                }
            }

            if (org != null) {
                org.getWorkQueue().getWorkRequestList().add(carrierWorkRequest);
                userAccount.getWorkQueue().getWorkRequestList().add(carrierWorkRequest);
            }
        JOptionPane.showMessageDialog(null, "Request created Successfully");
        //JOptionPane.showMessageDialog(null, "Request created Successfully", "Warning", JOptionPane.WARNING_MESSAGE);
        txtPlanCost.setText("");
        } else {
            JOptionPane.showMessageDialog(null, "Please select a plan and  check the cost");
        }
    }//GEN-LAST:event_btnRequestServiceActionPerformed

